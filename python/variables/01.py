# create a variable myInt that equals 4
myInt = 4
print('myInt')
print(myInt)

# update myInt to equal 1
myInt = 1
print('myInt')
print(myInt)

# increment myInt from 1 to 2
myInt = myInt + 1
print('myInt')
print(myInt)

# increment myInt from 2 to 3, this is shorthand
myInt += 1
print('myInt')
print(myInt)

# float
myFloat = 7.2
print('myFloat')
print(myFloat)

# subatract 3.2 from 7.2
myFloat = myFloat - 3.2
print('myFloat')
print(myFloat)

# shorthand for subtracting from variable
myFloat -= 1
print('myFloat')
print(myFloat)


# string
