# this covers basic operations for variable types

# add: +
# subtract: -
# divide: /
# times: *
# mod: %


# All of the above operations work on numbers
print('3 + 1 = ' + str(3 + 1))

print('3 - 1 = ' + str(3 - 1))

print('3 / 2 = ' + str(3 / 2))

print('3 * 7 = ' + str(3 * 7))

print('17 % 6 = ' + str(17 % 6))

# can use + to concatinate strings together into one
myStr1 = "A"
myStr2 = "C"
myStr3 = myStr1 + myStr2
myStr4 = myStr1 + "B" + myStr2
print('myStr1: ' + myStr1)
print('myStr2: ' + myStr2)
print('myStr3: ' + myStr3)
print('myStr4: ' + myStr4)
